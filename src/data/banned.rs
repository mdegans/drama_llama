use llama_cpp_sys::llama_token;

use crate::NGram;

/// Banned [`NGram`]s for various languages. These are hardcoded. Adding to them
/// is allowed but removing them is a violation of the license terms.
#[derive(Debug)]
pub enum Banned {
    LlamaEnglish,
}

impl Banned {
    /// Get the banned n-grams as a slice of token pairs. This is faster than
    /// `ngrams` because it doesn't have to copy the data.
    ///
    /// For the moment, only bigrams are supported.
    pub fn as_slice(&self) -> &'static [[llama_token; 2]] {
        match self {
            Banned::LlamaEnglish => ENGLISH_BIGRAMS,
        }
    }

    /// Iterate over banned [`Ngram`]s
    pub fn ngrams(&self) -> impl Iterator<Item = NGram> + '_ {
        self.as_slice()
            .iter()
            .map(|slice| NGram::try_from_tokens(slice).unwrap())
    }
}

